import UIKit

extension UIView {
    
    private func loadViewFromNib(nibName: String) -> UIView? {
        let nib = UINib(nibName: nibName, bundle: nil)
        return nib.instantiate(withOwner: self, options: nil).first as? UIView
    }
}
